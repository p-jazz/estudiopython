# Una de las caracteristicas más poderosas de los lenguajes de programaciónción como python, es su habilidad para manipular variables. Una variable es simplemente un nombre que referencia o apunta a un valor. Como su nombre lo indica,  el valor de la variable puede ir cambiando.

# Las vaiables permiten mantener valores en la memoria del computador 
name = "Estudiando Python"
name = name + "!"
print(name)

# Reasignación de variable. Ahora la variable apunta a un valor completamente diferente

name = "Hola Mundo"
print(name)

