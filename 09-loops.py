# Todos los programas o software ejecutan bloques de códigos en forma repetitiva hasta que cierta condición cambie. Python tiene 2 estructuras de control iterativas, el "while" y el "for"

# La estructura o loop while ejecuta las sentencias de suc uerpo mientras la expresión booleana sea verdadera:

num = 0
while num < 10:
    print(f"Esta es la vuelta número {num}")    
    num = num + 1