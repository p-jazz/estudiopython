# ejecucción condicional
#Todos los programas hacen evaluaciones condiconales según el valor de sus variables y sus expresiones. por lo tanto no todas las lineas de codigo se ejecutan cuando corren un programa.
# para esto está el concepto de Expresión booleana
#Ejemplo:

print(5 < 4) # False
print(5 > 4) # True
print(4 != 4) #False
print(4 == 4) #True

# Cuando queremos comparar menor o igual el operador es: <=
print(4 <= 6)
# El mayor igual es >=
print(5 >= 3)
# No existe el => ni =<

# Podemos tener expresiones booleanas mas complejas utilizando los opderadores and, or y not.

print(4 < 5 or 6 > 8) # True or falso


